CREATE TABLE MARKET(
  ID_MARKET INTEGER NOT NULL AUTO_INCREMENT,
  NAME_ VARCHAR(50) NOT NULL,
  NEMO VARCHAR(50) NOT NULL,
  DESCRIPTION VARCHAR(150) NOT NULL,
  PRIMARY KEY(ID_MARKET)
);

CREATE TABLE COMPANY(
  ID_COMPANY INTEGER NOT NULL AUTO_INCREMENT,
  ID_MARKET_ INTEGER,
  NAME_ VARCHAR(50) NOT NULL,
  SYMBOL VARCHAR(50) NOT NULL,
  DESCRIPTION VARCHAR(50) NOT NULL,
  PRIMARY KEY(ID_COMPANY),
  INDEX MARKET_INDEX(ID_MARKET_),
  FOREIGN KEY(ID_MARKET_)
);

CREATE TABLE STOCK(
  ID_STOCK INTEGER NOT NULL AUTO_INCREMENT,
  ID_COMPANY_ INTEGER,
  NAME_ VARCHAR(50) NOT NULL,
  SYMBOL VARCHAR(50) NOT NULL,
  DESCRIPTION VARCHAR(50) NOT NULL,
  PRIMARY KEY(ID_STOCK),
  INDEX COMPANY_INDEX(ID_COMPANY_),
  FOREIGN KEY(ID_COMPANY_)
);


CREATE TABLE QUOTE(
  ID_QUOTE INTEGER NOT NULL AUTO_INCREMENT,
  ID_STOCK_ INTEGER,
  ID_DATE_ INTEGER,
  LOW DOUBLE NOT NULL,
  HIGH DOUBLE NOT NULL,
  OPEN_ DOUBLE NOT NULL,
  CLOSE_ DOUBLE NOT NULL,
  VOLUME DOUBLE NOT NULL,
  PRIMARY KEY(ID_QUOTE),
  INDEX STOCK_INDEX(ID_STOCK_),
  INDEX DATE_INDEX(ID_DATE_),
  FOREIGN KEY(ID_STOCK_),
  FOREIGN KEY(ID_DATE_)
);

CREATE TABLE DATE_(
	ID_DATE INTEGER NOT NULL AUTO_INCREMENT,
	DATE_ INTEGER,
	MONTH_ INTEGER,
	YEAR_ INTEGER,
	PRIMARY KEY(ID_DATE)
);